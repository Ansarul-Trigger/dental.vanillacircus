  <script>
    const counters = document.querySelectorAll('.value');
    const speed = 200;
    const animateCounters = () => {
      counters.forEach(counter => {
        const target = +counter.textContent.replace(/,/g, '').replace('%', '');
        const isPercent = counter.textContent.includes('%');
        const isComma = counter.textContent.includes(',');
        counter.textContent = '0';
        const updateCount = () => {
          const current = +counter.textContent.replace(/,/g, '').replace('%', '');
          const increment = Math.ceil(target / speed);
          if (current < target) {
            const nextValue = current + increment;
            counter.textContent = isComma
              ? nextValue.toLocaleString()
              : nextValue + (isPercent ? '%' : '');
            setTimeout(updateCount, 20);
          } else {
            counter.textContent = isComma
              ? target.toLocaleString()
              : target + (isPercent ? '%' : '');
          }
        };
        updateCount();
      });
    };
    const statsSection = document.querySelector('.stats-section');
    let started = false;
    window.addEventListener('scroll', () => {
      const statsTop = statsSection.getBoundingClientRect().top;
      const windowHeight = window.innerHeight;
      if (!started && statsTop < windowHeight) {
        started = true;
        animateCounters();
      }
    });
  </script>

